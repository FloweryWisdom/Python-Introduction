#Request user to enter a number.
number = float(input("Enter a number: "))

#Perform the following operations on the number entered by the user.

double_number = number * 2
half_number = number / 2
difference_100 = number - 100
remainder_3 = number % 3

#Print the results of the operations.

print("The double of the number is: ", double_number)
print("The half of the number is: ", half_number)
print("The difference between the number and 100 is: ", difference_100)
print("The remainder of the number divided by 3 is: ", remainder_3)